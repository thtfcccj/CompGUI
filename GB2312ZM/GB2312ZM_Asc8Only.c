//使用缓冲
static unsigned char _Asc8Buf[8];
#include <string.h>

//--------------------------------获得英文字模-----------------------------
const unsigned char *GB2312ZM_pGetAsc8(char Asc)
{
  if((Asc < 32) || (Asc >= 127)) Asc = ' ' - 32;  //异常检查并纠错
  else Asc -= 32;
  _Asc8Buf[0] = 0;
  _Asc8Buf[7] = 0;
  memcpy(&_Asc8Buf[1], &_AscZM[Asc * 6], 6);
  return _Asc8Buf;
}

