static unsigned char _Asc8Buf[7];
#include <string.h>

//--------------------------------获得英文5*7字模--------------------------
const unsigned char *GB2312ZM_pGetAsc57(char Asc)
{
  if((Asc < 32) || (Asc >= 127)) Asc = ' ';  //异常检查并纠错
  
  const unsigned char *pZM = &_Asc57ZM[Asc - 32][0];
  
  //行列转换,如：  {0x00,0x00,0xFE,0x00,0x00},/*"|",44*/
  for(unsigned char x = 0; x < 7; x++){
    unsigned char yShift = 1 << (7 - x);    
    unsigned char ZM = 0;
    unsigned char xShift = 0x40; //左列为0x00丢弃
    for(unsigned char y = 0; y < 4; y++){
      if(*(pZM + y) & yShift) ZM |= xShift;
      xShift >>= 1;
    }
    //W,Y,#左右对称处理->可行，但增加了28字节，在字模上修改处理可行
    //if((Asc == '#') || (Asc == 'Y') || (Asc == 'w')){
    //  if(ZM & 0x08) ZM |= 0x80;
    //}
    _Asc8Buf[x] = ZM;
  }
  return _Asc8Buf;
}

